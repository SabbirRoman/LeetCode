class Solution {
public:
    int solve(int a, int b, int c) {
        // code here
        int ans=a+b+c;
        a=max({a,b,c});
        
        int tmp=((ans-a)*2)+2;
        if(a>tmp) ans=-1;
        
        return ans;
        
    }
};

